         <b>ÖDEME VE TESLİMAT</b>  <br>
            Ödeme Opsiyonları<br>
            
            Kredi Kartı<br>
            
            dafron.com‘da tüm Mastercard ve Visa kredi kartlarınızla alışveriş yapabilirsiniz.<br>
            
             
            
            Kapıda Ödeme<br>
            
            dafron.com‘dan verdiğiniz siparişlerde teslimat sırasında ödeme imkanınız da vardır. Kredi kartı ve nakit olarak alışverişlerinizde kapıda ödeme yapabilirsiniz. Kapıda ödemelerde kargo şirketi +5,90 TL tahsilat ücreti almaktadır.<br>
            
             
            
            Havale / EFT<br>
            
            Havale ile yaptığınız alışverişlerde, teslimat süresinin başlangıcı, siparişi verdiğiniz zaman değil sipariş tutarının havale ile hesaplarımıza geçtiği andır.<br>
            
            3 iş günü içinde hesaba geçmeyen havale siparişleri geçersiz sayılmaktadır.<br>
            
            Havale veya EFT ile yapılan alışverişlerde havale dekontunun tarafımıza e-mail veya faks ile iletilmesi gerekmektedir.<br>
            
             
            
            Anlaşmalı olduğumuz kargo şirketleri<br>
            
            Anlaşmalı olduğumuz kargo şirketleri MNG Kargo'dur.<br>
            
             
            
            Kredi Kartı Provizyon Aksaklıkları<br>
            
            Siparişiniz sırasında kredi kartınız farklı nedenlerden ötürü onaylanamayabilir: <br>
            Bu nedenler arasında başlıcaları;<br>
            
            1. Kredi kartınızın kullanılabilir limitinin yetersiz olması, <br>
            2. Kart numarasını hatalı girmeniz, <br>
            3. Kartınızın son kullanma tarihinin geçmiş olması, <br>
            4. Kullandığınız kart bir ek kart ise, ana kart sahibi tarafından iptal edilmiş olması, <br>
            5. Onay işlemi sırasında geçici bağlantı sorunları yaşanması, <br>
            6. Kayıp ihbarı yaptığınız bir kartı yeniden kullanmaya çalışmanız, <br>
            7. Kullanmaya çalıştığınız kartın bir kredi kartı değil, ATM kartı olması 
            sayılabilir.<br>
            
            Kredi kartı onay sorunlarınızda yardım almak için kartı aldığınız banka ile görüşmeniz gerekmektedir.<br>
            
            
            Üye Olmanın Avantajları<br>
            
            Satın aldığınız bir ürünün teslimatı için sizden istenen kimlik bilgileri, teslimat adresleri, irtibat numaraları vb. bilgileri her defasında yeniden yazmak zorunda kalmazsınız. Üye olurken bir defaya mahsus verdiğiniz bu bilgiler her alışverişinizde otomatik olarak yerini alır. Ayrıca bu bilgileri istediğiniz zaman güncelleyebilirsiniz. Her alışverişinizde bu bilgilerin otomatik olarak görüntülenmesi size hem zaman kazandırır hem de kolaylık sağlar.<br>
            
            dafron.com kapsamında düzenlenecek promosyonlardan, kampanyalardan ve indirimlerden ilk siz haberdar olacaksınız.<br>
            
            Bir de üye olarak yapacağınız her alışverişten puan kazanacaksınız.<br>
            
            Tavsiye Et<br>
            
            "Tavsiye Et" fonksiyonu sayesinde, satın almadığınız ancak arkadaşlarınızın ilgisini çekeceğini düşündüğünüz bir ürünü kolaylıkla onlara tavsiye edebilirsiniz.<br>
            
            
            Alışveriş Sepetiniz<br>
            
            dafron.com‘da yaptığınız alışveriş sırasında seçtiğiniz ürünleri sakladığınız bölümdür. Alınan ürünlere ve ödemelere ilişkin tüm bilgiler ödeme aşamasında topluca değerlendirilmek üzere buraya kaydedilir. Alışveriş arabanızı herhangi bir süpermarketten tanıdığınız geleneksel alışveriş arabasına benzetebilirsiniz.<br>
            
            
            <b> SİPARİŞ & TESLİMAT ŞARTLARI</b> <br>
            
            Siparişim elime ne zaman ulaşır?<br>
            
            Kargo teslimat süresi ürüne göre değişiklik gösterebilmektedir. Bazı özel tasarım ürünlerimizin hazırlanması ve yüzük ölçülerinin ayarlanması gibi unsurlar kargo süresini etkilemektedir.<br>
            
            
            Özel Sipariş<br>
            
            dafron.com stokta sürekli bulunmayan ve özel sipariş ibaresi bulunan ürünleri, müşterinin verdiği siparişe istinaden özel olarak ithal edilmektedir. Bu ürünlerin teslim süresi 7-10 iş günü içerisindedir. dafron.com bu süre içerisinde oluşan fiyat değişikliklerini ( KDV oranında oluşan değişiklikler hariç ) müşteriye yansıtmayacağını garanti eder.<br>
            
            
            Stokta Bulunmayan Ürünler<br>
            
            Verdiğiniz siparişlerde, stoklarımızda bulunduğunu belirttiğimiz halde herhangi bir sebepten dolayı stokta bulunmayan bulunmayan ürünler yüzünden gecikmeler yaşanabilir. Bu gibi durumlarda irtibat telefonlarınızdan veya e-postanızdan mutlaka size ulaşılarak haber verilecektir. Size ulaşabilmemiz için lütfen kişisel bilgilerinizi eksiksiz doldurunuz. Teslimatla ilgili yaşanabilecek gecikmeler tarafınıza bildirilecek ve kabul etmeniz halinde siparişiniz onaylanacaktır. Gecikme halini kabul etmemeniz durumunda, alınan ürün bedelinin tamamı hesabınıza iade edilecektir.<br>
            
            
            Adres Bilgileri<br>
            
            Siparişinizin size en çabuk şekilde ulaşması için lütfen adres bilgilerinizi mümkün olduğu kadar açık yazınız.<br>
            
            
            Hatalı / Eksik Siparişler<br>
            
            …. Kargo‘dan teslim aldığınız paketi kargo görevlisinin yanında açıp, siparişinizi kontrol ediniz. Herhangi bir yanlışlık veya eksiklik gördüğünüz takdirde, derhal bizimle irtibata geçiniz. Paketi açılmış veya hasar görmüş kargoları, kargo yetkilisiyle rapor edip, derhal bizimle irtibata geçiniz.<br>
            
            
            Kargo Ücreti<br>
            
            İnternet sitemizden yapılan bütün alışverişlerde kargo ücretsizdir. Ürün bedeli dışında hiçbir ücret ödemezsiniz. Ancak kapıda ödeme hizmeti isterseniz bu hizmet +5,90 TL ile ücretlendirilir. Bu ücret kargo şirketi tarafından "para taşıma hizmeti" olarak alınmaktadır.<br>
            
            
